class Universidade{
	private String nome;
	private String cidade;

	public void setUniversidade(String nome){
		this.nome = nome;
	}

	public String getUniversidade(){
		return this.nome;
	}

	public void setCidade(String cidade){
		this.cidade = cidade;
	}

	public String getCidade(){
		return this.cidade;
	}
}